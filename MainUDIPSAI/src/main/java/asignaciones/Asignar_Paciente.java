    private void btn1_busespActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1_busespActionPerformed

        Connection con = null;
        try {
            con = conexion();
            //Preparo una consulta:vamos a buscar por cedula
            ps = con.prepareStatement("select * from especialista inner join especialidad on  especialista.id_especialidad=especialidad.id where cedula=?");
            ps.setString(1, txt_cedulaesp.getText());
            //traiga Resultados de la consulta
            rs = ps.executeQuery();

            if (rs.next()) {
                txt_nomesp.setText(rs.getString("primerNombre"));
                txt_apeesp.setText(rs.getString("primerApellido"));
                txt_areaesp.setText(rs.getString("area"));
            } else {
                JOptionPane.showMessageDialog(null, "No existe un especialista con ese número de cédula");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Asignar_Paciente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn1_busespActionPerformed

