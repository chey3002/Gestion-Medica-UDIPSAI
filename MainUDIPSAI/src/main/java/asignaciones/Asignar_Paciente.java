    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        try {
            // Verificar si los campos están llenos
            String ficha = txt_ficha.getText();
            String cedulaEsp = txt_cedulaesp.getText();

            if (ficha.isEmpty() || cedulaEsp.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Los campos estan vacios");
            } else {
                Connection con = null;
                try {
                    con = conexion();

                    // Preparar la consulta de inserción
                    ps = con.prepareStatement("INSERT INTO asignaciones (idPaciente, cedulaEspecialista) VALUES (?, ?)");
                    ps.setString(1, ficha);
                    ps.setString(2, cedulaEsp);

                    int res = ps.executeUpdate();

                    // Verificar el resultado de la ejecución
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Los datos fueron guardados correctamente");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al guardar los datos");
                    }
                    // Verificar si es especialista o pasante
                    PreparedStatement psEsp = con.prepareStatement("SELECT esPasante, especialistaAsignado FROM especialista WHERE cedula = ?");
                    psEsp.setString(1, cedulaEsp);
                    ResultSet rsEsp = psEsp.executeQuery();
                    if (rsEsp.next()) {
                        String esPasante = rsEsp.getString("esPasante");
                        if (esPasante.equals("true")) {
                            String especialistaAsignado=rsEsp.getString("especialistaAsignado");

                            // Asignar el especialista al paciente
                            ps = con.prepareStatement("INSERT INTO asignaciones (idPaciente, cedulaEspecialista) VALUES (?, ?)");
                            ps.setString(1, ficha);
                            ps.setString(2, especialistaAsignado);
                            res = ps.executeUpdate();
                            // Verificar el resultado de la ejecución
                            if (res > 0) {
                                JOptionPane.showMessageDialog(null, "El especialista fue correctamente asignado al especialista asignado al pasante");
                            } else {
                                JOptionPane.showMessageDialog(null, "Error al asignar un especialista al pasante con un paciente");
                            }
                        }
                    }

                    con.close();
                } catch (SQLException ex) {
                    System.out.println(ex);
                    JOptionPane.showMessageDialog(null, "Error al guardar los datos");
                }
            }
        } catch (HeadlessException ex) {
            
            JOptionPane.showMessageDialog(null, "Error al guardar los datos");
        }
    }//GEN-LAST:event_GuardarActionPerformed

