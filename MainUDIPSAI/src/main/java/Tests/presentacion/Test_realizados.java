    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int col = jTable1.columnAtPoint(evt.getPoint());
        int row = jTable1.rowAtPoint(evt.getPoint());
        //dowload file
        if (col == 3) {
            try {
                new GestorTest().DescargarPDF(idList[row]);
                JOptionPane.showMessageDialog(null, "El archivo se descargado correctamente.", "ConfirmaciÃ³n", JOptionPane.INFORMATION_MESSAGE);

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error al descargar el archivo: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        //open File
        if (col == 4) {
            try{
                new GestorTest().AbrirPDF(idList[row]);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error al abrir el archivo: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        //Delete file
        if (col == 5) {
            int opcion = JOptionPane.showOptionDialog(
                    null,
                    "Â¿Desea eleminar este test?",
                    "ConfirmaciÃ³n",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    new Object[]{"SÃ­", "No"},
                    "No"
            );

            if (opcion == JOptionPane.YES_OPTION) {
                new GestorTest().eliminarTest(idList[row]);
                Test_realizados tests_Realizados = new Test_realizados(sesion, paciente);
                tests_Realizados.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE); // Establecer acciÃ³n de cierre
                tests_Realizados.setVisible(true); // Mostrar el JFrame NuevoFrame
                this.dispose();

            }
        }
        


    }//GEN-LAST:event_jTable1MouseClicked

